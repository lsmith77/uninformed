<h3><a href="#">What is resolutionfinder.org?</a></h3>
<div>
    The resolutionfinder.org application is a database concept which was developed across four thematic issues chosen
    from the UN agenda. These four thematic issues are: 1) clean drinking water 2) women and education 3) malaria 4)
    small arms and light weapons. After testing the viability of our search options and filters, we will expand the
    content of resolutionfinder.org to include further issues. Our long-term goal is to ultimately cover all thematic
    areas of the UN and contain information on several tens of thousands of different documents.
</div>

<h3><a href="#">Who is behind resolutionfinder.org?</a></h3>
<div>
    Making Commitments Matter (MCM) is an international research and development initiative composed of graduate and
    undergraduate students as well as young professionals. Our goal is to improve the national implementation of
    resolutions and conventions issued by the United Nations and its Specialised Agencies by making relevant information
    more accessible.
</div>

<h3><a href="#">Which documents can I find on resolutionfinder.org?</a></h3>
<div>
    <h4>UN agreements</h4>

    <div>The general term "UN agreements" is used to describe the sum of legally binding and non-legally binding
    agreements negotiated within the UN system.</div>

    <h4><a href="#" class="toggleCol" target="which_binding">legally binding agreement</a></h4>

    <div id="which_binding" class="collapsed">In terms of legally binding agreements, Making Commitments Matter has considered multilateral conventions of
    potential universal adherence registered with the UN in accordance with Art. 102 of the UN Charter or deposited
    with the Secretary-General of the UN.</div>

    <h4><a href="#" class="toggleCol" target="which_nonbinding">non legally binding agreements</a></h4>

    <div id="which_nonbinding" class="collapsed">Non-legally binding agreements are all documents that have been negotiated by states and are also addressed to
    them, but are not by their form legally binding. Non-legally binding agreements consequently encompass international
    agreements negotiated under the auspices of the UN at global summits and other multilateral gatherings. They also
    cover UN resolutions passed by the main United Nations organs and their subsidiary bodies, as well as
    recommendations, regulations, resolutions, plans of actions and other types of agreements issued by Specialised and
    Related Agencies.</div>

    <h4><a href="#" class="toggleCol" target="which_sc">Security Council resolutions</a></h4>

    <div id="which_sc" class="collapsed">Security Council resolutions are left out of the above classification into legally binding or non legally binding
    documents. While they constitute UN agreements, they cannot be classified into a category merely by their form.
    Therefore, Making Commitments Matter has refrained from assigning them a legal value, and provisions from Security
    Council resolutions are marked differently, so as to make the specific nature of these agreements visible.</div>

    <h4><a href="#" class="toggleCol" target="which_not">agreements not negotiated within the UN system</a></h4>

    <div id="which_not" class="collapsed">As a general rule, Making Commitments Matter does not consider international agreements from outside the UN
    system. However, evaluation of the research showed that there are certain agreements not issued within the UN system
    that are nevertheless connected to the UN. These are documents that are negotiated by states, and additionally are
    either global in nature (i.e. negotiated in a multilateral forum outside of the UN system) or have been recalled in
    a UN agreement (i.e. they have been processed to the UN by the relevant regional authority by way of an annex to a
    letter, and have been assigned a UN document number). These documents were also included into Making Commitments
    Matter's UN Agreements research.</div>
</div>

<h3><a href="#">What are information types?</a></h3>
<div>
    <div>In line with Making Commitments Matter's main underlying assumption that easier access to relevant information
    will enhance national implementation of these agreements, research focused on identifying such information, based on
    formal linguistic criteria, and classifying it into what we call "information type"</div>

    <h4><a href="#" class="toggleCol" target="infotypes_bystates">Commitments made by states</a></h4>

    <div id="infotypes_bystates" class="collapsed">"Commitments made by states" are understood as clauses from UN agreements containing measures that need to be
    carried out by states. The addressee of such a clause thus needs to be the states themselves. From a legal point of
    view, it could be contested that such clauses reflect a legally relevant agreement, since they technically reflect
    the view of the respective issuing organ. However, the fact that the issuing organ is in fact composed of state
    representatives needs to be taken into consideration: Regardless of any legal qualification, clauses adopted by
    states and addressed at states can be considered as a commitment on a political level. Commitments made by states
    are divided into "national level" and "international level", depending on whether the measure needs to be
    implemented domestically or regionally/globally.</div>

    <h4><a href="#" class="toggleCol" target="infotypes_further">Further recommendations for implementation</a></h4>

    <div id="infotypes_further" class="collapsed">While a large amount of clauses contain measures to be undertaken by states, they are not necessarily
    commitments in the sense that they merely recommend action to states. Such clauses are considered as constituting
    "further recommendations for implementation".</div>

    <h4><a href="#" class="toggleCol" target="infotypes_organ">Clauses addressed to an organ within the United Nations system</a></h4>

    <div id="infotypes_organ" class="collapsed">Clauses addressed to an organ within the UN System, i.e. such clauses that specifically address the part of the
    UN System (be it an organ of an international organization, or specific parts of the various Secretariats of
    international organizations), have been defined as "request to UN institution".</div>

    <h4><a href="#" class="toggleCol" target="infotypes_actors">Clauses addressed to actors other than states</a></h4>

    <div id="infotypes_actors" class="collapsed">Lastly, clauses addressed to actors other than states are split into requests and recommendations to actors other
    than states. Due to the great diversity of language used when determining the addressee, no further differentiation
    as to the term "actors other than states" was developed.</div>
</div>

